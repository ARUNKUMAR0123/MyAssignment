package Week1.day3;

public class PositiveNegative {
  public static void main(String[] args) {
	int n = 9;
	int i = -6;
	if (n>=0) {
		System.out.println("n : "+ "The number is Positive number");
	}	
	else {
		System.out.println("n : "+ "The number is Negative number");
	}
	if (i>=0) {
		System.out.println("i : " + "The number is Positive number");
	}
	else {
		System.out.println("i : " + "The number is Negative number");
		
		}
	
}
}
